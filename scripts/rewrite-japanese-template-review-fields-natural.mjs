function getValue(block, label) {
  const match = block.match(new RegExp(`^- ${escapeRegex(label)}：([^\\n]*)`, "m"));
  return match ? match[1].trim() : "";
}

function setValue(block, label, value, afterLabel) {
  const line = `- ${label}：${value}`;
  const pattern = new RegExp(`^- ${escapeRegex(label)}：[^\\n]*`, "m");
  if (pattern.test(block)) {
    return block.replace(pattern, line);
  }
  const after = new RegExp(`^- ${escapeRegex(afterLabel)}：[^\\n]*`, "m");
  if (after.test(block)) {
    return block.replace(after, (matched) => `${matched}\n${line}`);
  }
  return `${block.trimEnd()}\n${line}\n`;
}

function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function effectiveName(block) {
  return getValue(block, "修改后材料名") || getValue(block, "原材料名");
}

function fallbackName(name) {
  return `${name}（要確認）`;
}

function fallbackDescription(name) {
  return `${japaneseNames.get(name) ?? name}について、申請内容に応じて必要となる資料を提出します。発行者、発行日、対象期間及び記載内容が審査上確認できる状態で準備します。`;
}

