function pickValue(block, changedLabel, originalLabel) {
  const changed = getValue(block, changedLabel);
  return changed || getValue(block, originalLabel);
}

function getValue(block, label) {
  const match = block.match(new RegExp(`^- ${escapeRegex(label)}：([^\\n]*)`, "m"));
  return match ? match[1].trim() : "";
}

function setValue(block, label, value, afterLabel) {
  const line = `- ${label}：${value}`;
  const pattern = new RegExp(`^- ${escapeRegex(label)}：[^\\n]*`, "m");
  if (pattern.test(block)) {
    return block.replace(pattern, line);
  }
  const after = new RegExp(`^- ${escapeRegex(afterLabel)}：[^\\n]*`, "m");
  if (after.test(block)) {
    return block.replace(after, (matched) => `${matched}\n${line}`);
  }
  return `${block.trimEnd()}\n${line}\n`;
}

function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function translateName(value) {
  if (!value || value === "无") {
    return "";
  }
  if (nameJa.has(value)) {
    return nameJa.get(value);
  }
  return applyGlossary(value);
}

function translateDescription(value) {
  if (!value || value === "无") {
    return "";
  }
  for (const [zh, ja] of phraseJa) {
    if (value === zh) {
      return ja;
    }
  }
  return applyGlossary(value)
    .replace(/。/g, "。")
    .replace(/，/g, "、")
    .replace(/；/g, "。")
    .replace(/：/g, "：");
}

function applyGlossary(value) {
  let translated = value;
  for (const [zh, ja] of glossary) {
    translated = translated.replaceAll(zh, ja);
  }
  return translated;
}

const source = await fs.readFile(inputPath, "utf8");
const blocks = source.split(/(?=^#### )/m);
let materialBlocks = 0;
let mappedNames = 0;
let autoNames = 0;
let descriptions = 0;

const updated = blocks.map((block) => {
  if (!block.startsWith("#### ")) {
    return block;
  }

  materialBlocks += 1;
  const effectiveName = pickValue(block, "修改后材料名", "原材料名");
  const effectiveDescription = pickValue(block, "修改后说明", "原说明");
  const japaneseName = translateName(effectiveName);
  const japaneseDescription = translateDescription(effectiveDescription);

  if (nameJa.has(effectiveName)) {
    mappedNames += 1;
  } else {
    autoNames += 1;
  }
  if (japaneseDescription) {
    descriptions += 1;
  }

  let next = block;
  next = setValue(next, "日语材料名", japaneseName, "修改后材料名");
  next = setValue(next, "修改后日语材料名", "", "日语材料名");
  next = setValue(next, "日语说明", japaneseDescription, "修改后说明");
  next = setValue(next, "修改后日语说明", "", "日语说明");
  return next;
}).join("");

