export function displayLocalizedLabel(
  value: string | null | undefined,
  locale: AppLocale = DEFAULT_LOCALE,
): string {
  if (!value) {
    return "-";
  }
  const localeLabels: Record<string, string> = labels[locale];
  return localeLabels[value] ?? localeLabels[casePhaseAliases[value] ?? ""] ?? displayChineseText(value);
}

export function displayLocalizedCasePhaseLabel(
  value: string | null | undefined,
  locale: AppLocale = DEFAULT_LOCALE,
): string {
  return displayLocalizedLabel(value, locale);
}

export function displayLocalizedGroupTitle(
  value: string,
  locale: AppLocale = DEFAULT_LOCALE,
): string {
  const localeGroupTitles: Record<string, string> = groupTitles[locale];
  return localeGroupTitles[value] ?? localeGroupTitles.other;
}
